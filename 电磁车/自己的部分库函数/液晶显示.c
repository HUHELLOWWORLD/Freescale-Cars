/*--------------------*OLED写命令*--------------------*/

void OLED_Write_Command(unsigned char Cmd)
{
	unsigned char Temp_i=8;
	
	LCD_DC=0;;
  LCD_SCL=0;;
  while(Temp_i--)
  {
    if(Cmd&0x80){LCD_SDA=1;}
    else{LCD_SDA=0;;}
    LCD_SCL=1;;
    asm("nop");;
    LCD_SCL=0;;    
    Cmd<<=1;;   
  } 	
}



void OLED_Set_Pos(unsigned char x,unsigned char y)
{ 
  OLED_Write_Command(0xb0+y);
  OLED_Write_Command(((x&0xf0)>>4)|0x10);
  OLED_Write_Command(x&0x0f); 
} 





/*---------------------*列表移动*---------------------*/      
signed char Temp_List[120];
char Change_To_Bit[8]=
{
  0x80,0x40,0x20,0x10,0x08,0x04,0x02,0x01
};

void List_Move(signed char New_Data)
// 对数组进行移位
{
  static signed int Temp_i;
  signed int Temp_j;
  if(Temp_i<120)
    Temp_List[Temp_i]=New_Data;
  else
  {
    for(Temp_j=0;Temp_j<119;Temp_j++)
      Temp_List[Temp_j]=Temp_List[Temp_j+1];  
    Temp_List[119]=New_Data;  
  }

  if(Temp_i>=120)
    Temp_i=120;
  else
    Temp_i++; 
}
/*-------------------*显示列表图形*-------------------*/      

void List_Paint(void)
// 将Temp_List列表里面的数据用一整幅图画出来
{
  unsigned char Temp_i;
  unsigned char Cross;  

  for(Cross=0;Cross<6;Cross++)
  {
    OLED_Set_Pos(4,7-Cross); 
    for(Temp_i=0;Temp_i<120;Temp_i++) 
    {
      if(Temp_List[Temp_i]>>3==Cross)
      {
        LCD_WrDat(Change_To_Bit[ Temp_List[Temp_i]&0x07 ]);   
      }
      else 
      {
        LCD_WrDat(0x00);
      }     
    }    
  }
}
/*-------------------*递推显示数据*-------------------*/      
 
  
void Paint_List(float New_Data,float Paint_Ave,float Paint_Margin)
// 将某个数据以列表刷新的形式显示出来
{
  unsigned char Temp_Data;
  
  Temp_Data=(signed char)(22.0/Paint_Margin*(New_Data-Paint_Ave)+26);
  List_Move(Temp_Data);
  List_Paint();
}